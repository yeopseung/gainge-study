/**
 * 변수 선언하기
 * 
 * 1) var - 안씀, 선언 후 값 변경 가능
 * 2) let - 선언 후 값 변경 가능
 * 3) const - 선언 후 값 변경 불가능
 * 
 * => 웬만해서는 const로 선언하고 나머지를 let으로
 * 
 */

/**
 * 선언과 할당
 * 
 * 1) 선언 - 변수를 선언하는 것
 * 2) 할당 - 변수에 값을 넣어주는 것
 * 
 */

var name = '곽승엽';
name = '곽엽승';
console.log(name);

let ive = '아이브';
ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
// newJeans = '하니';
console.log(newJeans);

